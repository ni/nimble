export type DrawerLocationAttribute = 'left' | 'right';
export enum DrawerLocation {
    Left = 'left',
    Right = 'right'
}

export type DrawerStateAttribute = 'opening' | 'opened' | 'closing' | 'closed';
export enum DrawerState {
    Opening = 'opening',
    Opened = 'opened',
    Closing = 'closing',
    Closed = 'closed'
}
